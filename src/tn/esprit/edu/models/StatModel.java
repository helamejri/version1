package tn.esprit.edu.models;

public class StatModel {
    private int idcat;
    private int idCatCount;
    private String categorie;
    private String libmois;
    private int mois;

    public int getIdcat() {
        return idcat;
    }

    public void setIdcat(int idcat) {
        this.idcat = idcat;
    }

    public int getIdCatCount() {
        return idCatCount;
    }

    public void setIdCatCount(int idCatCount) {
        this.idCatCount = idCatCount;
    }

    public String getCategorie() {
        return categorie;
    }

    public void setCategorie(String categorie) {
        this.categorie = categorie;
    }

    public String getLibmois() {
        return libmois;
    }

    public void setLibmois(String libmois) {
        this.libmois = libmois;
    }

    public int getMois() {
        return mois;
    }

    public void setMois(int mois) {
        this.mois = mois;
    }
}
